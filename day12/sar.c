#include "struct.h"

int main(int ac, char **av)
{
    if(ac == 4)
    {
        if(!av[1] || av[2][1] || av[3][1])
            return 1;
            
        int i = 0;
        char *s = av[1];
        
        while(s[i])
        {
            if(s[i] == av[2][0])
                s[i] = av[3][0];
            write(1, &s[i++], 1);
        }
    }
    write(1, "\n", 1);
    return 0;
}