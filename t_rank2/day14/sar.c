#include "struct.h"

int main(int ac, char **av)
{
    if(ac == 4 && !av[2][1] && !av[3][1])
    {
        int i = 0;
        char *str = av[1];
        
        while(str[i])
        {
            if(str[i] == av[2][0])
            {
                str[i] = av[3][0];
            }
            write(1, &str[i++], 1);
        }
    }
    write(1, "\n", 1);
    return 0;
}