#include "struct.h"

int main(int ac, char **av)
{
    if(ac == 4 && av[2][0] && av[3][0])
    {
        int i = 0;
        char *s1 = av[1];

        while(s1[i])
        {
            if(s1[i] == av[2][0])
            {
                s1[i] = av[3][0];
            }
            write(1, &s1[i++], 1);
        }
    }
    write(1, "\n", 1);
    return 0;
}