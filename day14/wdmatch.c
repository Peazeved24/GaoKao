#include "struct.h"


int main(int ac, char **av)
{
    if(ac == 3)
    {
        int i= 0;
        char *s1 = av[1];
        char *s2 = av[2];

        while(s1[i] && *s2)
        {
            if(s1[i]== *s2)
                i++;
            s2++;
        }
        if(s1[i] == '\0')
            write(1, s1, i);
    }
    
    write(1, "\n", 1);
    return 0;
}